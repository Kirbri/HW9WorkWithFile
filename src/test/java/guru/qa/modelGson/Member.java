package guru.qa.modelGson;

public class Member {
    private String name;
    private Integer age;
    private String secretIdentity;
    private String[] powers;

    public String getName() {
        return name;
    }

    public Integer getAge() {
        return age;
    }

    public String getSecretIdentity() {
        return secretIdentity;
    }

    public String[] getPowers() {
        return powers;
    }
}